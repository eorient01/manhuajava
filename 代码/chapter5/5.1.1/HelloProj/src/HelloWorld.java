
/*
 *  5.1.1  一元运算符 示例
 */

public class HelloWorld {
    public static void main(String[] args) {
        int a = 18;
        System.out.printf("-a为%d%n", -a);

        System.out.printf("++a为%d%n", ++a);
        System.out.printf("a++为%d%n", a++);
        System.out.printf("a为%d%n", a);

    }
}

